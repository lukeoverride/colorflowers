package flowerymeadow;

public class Advice {
	
	private int x;
	private int y; 
	private int help;
	
	public Advice(int x, int y, int help) {
		super();
		this.x = x;
		this.y = y;
		this.help = help;
	}
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getHelp() {
		return help;
	}
	public void setHelp(int help) {
		this.help = help;
	}
	
	

}
